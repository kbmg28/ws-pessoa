package br.com.kbmg.dto;

public class PessoaFisicaDto {

	private Long idPf;
	private String cpf;
	private String rg;

	public Long getIdPf() {
		return idPf;
	}

	public void setIdPf(Long idPf) {
		this.idPf = idPf;
	}

	public String getCpf() {
		return cpf;
	}

	public void setCpf(String cpf) {
		this.cpf = cpf;
	}

	public String getRg() {
		return rg;
	}

	public void setRg(String rg) {
		this.rg = rg;
	}

}
